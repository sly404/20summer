package com.manager.login.Entity;

public class Dish {
    private int dish_id;
    private String dish_name;
    private float dish_price;
    private float dish_discount;
    private String dish_description;
    private String dish_type;
    private String dish_quantity;
    private int dish_state;
    private String dish_picture;

    public int getDish_id() {
        return dish_id;
    }
    public void setDish_id(int dish_id) {
        this.dish_id = dish_id;
    }
    public String getDish_name() {
        return dish_name;
    }
    public void setDish_name(String dish_name) {
        this.dish_name = dish_name;
    }
    public float getDish_price() {
        return dish_price;
    }
    public void setDish_price(float dish_price) {
        this.dish_price = dish_price;
    }
    public float getDish_discount() {
        return dish_discount;
    }
    public void setDish_discount(float dish_discount) {
        this.dish_discount = dish_discount;
    }
    public String getDish_description() {
        return dish_description;
    }
    public void setDish_description(String dish_discription) {
        this.dish_description = dish_discription;
    }
    public String getDish_type() {
        return dish_type;
    }
    public void setDish_type(String dish_type) {
        this.dish_type = dish_type;
    }
    public String getDish_quantity() {
        return dish_quantity;
    }
    public void setDish_quantity(String dish_quantity) {
        this.dish_quantity = dish_quantity;
    }
    public int getDish_state() {
        return dish_state;
    }
    public void setDish_state(int dish_state) {
        this.dish_state = dish_state;
    }
    public String getDish_picture() {
        return dish_picture;
    }
    public void setDish_picture(String dish_picture) {
        this.dish_picture = dish_picture;
    }
}
